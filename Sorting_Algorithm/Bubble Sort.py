
def bubble_sort(arr):
    for j in range(len(arr)-1, 0, -1):
        j < len(arr)
        for i in range(j):
            i < len(arr)
            if arr[i] > arr[i + 1]:
                arr[i], arr[i + 1] = arr[i + 1], arr[i]
            else:
                print(arr)
                print()



arr = [1, 3, 2, 5, 4]
bubble_sort(arr)
print(arr)